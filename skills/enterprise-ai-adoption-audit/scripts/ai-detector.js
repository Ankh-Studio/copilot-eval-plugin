#!/usr/bin/env node

/**
 * AI Technology Detection Script
 *
 * Analyzes repository for AI/ML technologies, frameworks, and integrations
 * to assess AI readiness and capability maturity.
 */

const fs = require('fs');
const path = require('path');

class AITechnologyDetector {
  constructor(repoPath = '.') {
    this.repoPath = repoPath;
    this.findings = {
      languages: new Set(),
      frameworks: new Set(),
      aiLibraries: new Set(),
      aiServices: new Set(),
      aiConfigurations: new Set(),
      aiGeneratedPatterns: new Set(),
      infrastructure: new Set(),
      dependencies: new Map(),
    };
  }

  /**
   * Core detection methods
   */
  async detect() {
    console.log('🔍 Starting AI Technology Detection...');

    await this.scanRepository();
    await this.analyzeDependencies();
    await this.detectAIConfigurations();
    await this.scanForAIGeneratedCode();
    await this.analyzeInfrastructure();

    return this.generateReport();
  }

  async scanRepository() {
    console.log('📁 Scanning repository structure...');

    const files = this.getAllFiles(this.repoPath);

    for (const file of files) {
      const ext = path.extname(file);
      const content = await this.readFile(file);

      // Language detection
      this.detectLanguages(ext, content);

      // Framework detection
      this.detectFrameworks(file, content);

      // AI/ML library detection
      this.detectAILibraries(file, content);
    }
  }

  async analyzeDependencies() {
    console.log('📦 Analyzing dependencies...');

    const packageFiles = [
      'package.json',
      'requirements.txt',
      'Pipfile',
      'Cargo.toml',
      'pom.xml',
      'build.gradle',
    ];

    for (const pkgFile of packageFiles) {
      const filePath = path.join(this.repoPath, pkgFile);
      if (fs.existsSync(filePath)) {
        await this.parsePackageFile(filePath);
      }
    }
  }

  async detectAIConfigurations() {
    console.log('⚙️ Detecting AI configurations...');

    const configPatterns = [
      '**/.copilot/**',
      '**/ai-config.*',
      '**/ml-config.*',
      '**/model.*.json',
      '**/ai-settings.*',
      '**/.ai/**',
    ];

    for (const pattern of configPatterns) {
      const files = this.glob(pattern);
      for (const file of files) {
        this.findings.aiConfigurations.add(file);
        await this.analyzeAIConfig(file);
      }
    }
  }

  async scanForAIGeneratedCode() {
    console.log('🤖 Scanning for AI-generated code patterns...');

    const aiPatterns = [
      /Generated by Copilot/gi,
      /AI-generated/gi,
      /This code was generated by/gi,
      /Created with assistance from/gi,
      /AI-assisted development/gi,
      /Automatically generated/gi,
    ];

    const files = this.getAllFiles(this.repoPath);

    for (const file of files) {
      const content = await this.readFile(file);

      for (const pattern of aiPatterns) {
        if (pattern.test(content)) {
          this.findings.aiGeneratedPatterns.add(file);
          break;
        }
      }
    }
  }

  async analyzeInfrastructure() {
    console.log('🏗️ Analyzing infrastructure...');

    const infraFiles = [
      'docker-compose.yml',
      'Dockerfile',
      'kubernetes/',
      '.github/workflows/',
      'terraform/',
      'cloudformation/',
      'azure-pipelines.yml',
    ];

    for (const infraFile of infraFiles) {
      const filePath = path.join(this.repoPath, infraFile);
      if (fs.existsSync(filePath)) {
        await this.analyzeInfraFile(filePath);
      }
    }
  }

  /**
   * Detection helpers
   */
  detectLanguages(ext, content) {
    const languageMap = {
      '.js': 'JavaScript',
      '.ts': 'TypeScript',
      '.py': 'Python',
      '.java': 'Java',
      '.cs': 'C#',
      '.cpp': 'C++',
      '.go': 'Go',
      '.rs': 'Rust',
      '.rb': 'Ruby',
      '.php': 'PHP',
    };

    if (languageMap[ext]) {
      this.findings.languages.add(languageMap[ext]);
    }
  }

  detectFrameworks(file, content) {
    const frameworkPatterns = {
      React: /import.*react|from ['"]react['"]/gi,
      Vue: /import.*vue|from ['"]vue['"]/gi,
      Angular: /import.*@angular|from ['"]@angular['"]/gi,
      TensorFlow: /import.*tensorflow|from ['"]tensorflow['"]/gi,
      PyTorch: /import.*torch|from ['"]torch['"]/gi,
      'scikit-learn': /import.*sklearn|from ['"]sklearn['"]/gi,
      FastAPI: /from fastapi|import fastapi/gi,
      Flask: /from flask|import flask/gi,
      Django: /from django|import django/gi,
    };

    for (const [framework, pattern] of Object.entries(frameworkPatterns)) {
      if (pattern.test(content)) {
        this.findings.frameworks.add(framework);
      }
    }
  }

  detectAILibraries(file, content) {
    const aiLibraryPatterns = {
      OpenAI: /openai/gi,
      Anthropic: /anthropic/gi,
      'Hugging Face': /transformers|datasets|huggingface/gi,
      LangChain: /langchain/gi,
      'Azure AI': /azure.*ai|azure.*cognitive/gi,
      'Google AI': /google.*ai|tensorflow/gi,
      'AWS AI': /aws.*ai|sagemaker|comprehend/gi,
      Pandas: /import pandas|from pandas/gi,
      NumPy: /import numpy|from numpy/gi,
      Matplotlib: /import matplotlib|from matplotlib/gi,
      Jupyter: /jupyter|ipynb/gi,
    };

    for (const [library, pattern] of Object.entries(aiLibraryPatterns)) {
      if (pattern.test(content)) {
        this.findings.aiLibraries.add(library);
      }
    }
  }

  async parsePackageFile(filePath) {
    const content = await this.readFile(filePath);
    const ext = path.extname(filePath);

    try {
      let dependencies = {};

      switch (ext) {
        case '.json':
          const pkg = JSON.parse(content);
          dependencies = { ...pkg.dependencies, ...pkg.devDependencies };
          break;
        case '.txt':
          dependencies = this.parseRequirementsTxt(content);
          break;
        case '.toml':
          dependencies = this.parseToml(content);
          break;
      }

      for (const [dep, version] of Object.entries(dependencies)) {
        this.findings.dependencies.set(dep, version);

        // Check for AI-related packages
        if (this.isAIPackage(dep)) {
          this.findings.aiLibraries.add(dep);
        }
      }
    } catch (error) {
      console.warn(`Failed to parse ${filePath}:`, error.message);
    }
  }

  async analyzeAIConfig(filePath) {
    const content = await this.readFile(filePath);

    // Detect AI service configurations
    const servicePatterns = {
      OpenAI: /openai|gpt-/gi,
      'Azure AI': /azure.*ai|cognitive/gi,
      'Google AI': /google.*ai|vertex/gi,
      'AWS AI': /aws.*ai|sagemaker/gi,
      'Hugging Face': /huggingface/gi,
    };

    for (const [service, pattern] of Object.entries(servicePatterns)) {
      if (pattern.test(content)) {
        this.findings.aiServices.add(service);
      }
    }
  }

  async analyzeInfraFile(filePath) {
    const content = await this.readFile(filePath);

    // Detect AI/ML infrastructure
    const infraPatterns = {
      Docker: /docker/gi,
      Kubernetes: /kubernetes|k8s/gi,
      GPU: /gpu|cuda/gi,
      'Cloud AI': /sagemaker|vertex|azure.*ml/gi,
      'CI/CD': /github.*actions|gitlab.*ci|jenkins/gi,
    };

    for (const [infra, pattern] of Object.entries(infraPatterns)) {
      if (pattern.test(content)) {
        this.findings.infrastructure.add(infra);
      }
    }
  }

  /**
   * Utility methods
   */
  getAllFiles(dir, fileList = []) {
    const files = fs.readdirSync(dir);

    for (const file of files) {
      const filePath = path.join(dir, file);
      const stat = fs.statSync(filePath);

      if (
        stat.isDirectory() &&
        !file.startsWith('.') &&
        file !== 'node_modules'
      ) {
        this.getAllFiles(filePath, fileList);
      } else if (stat.isFile()) {
        fileList.push(filePath);
      }
    }

    return fileList;
  }

  glob(pattern) {
    // Simple glob implementation for common patterns
    const files = this.getAllFiles(this.repoPath);
    const regex = new RegExp(
      pattern.replace(/\*\*/g, '.*').replace(/\*/g, '[^/]*')
    );

    return files.filter(file => regex.test(file));
  }

  async readFile(filePath) {
    try {
      return fs.readFileSync(filePath, 'utf8');
    } catch (error) {
      console.warn(`Failed to read ${filePath}:`, error.message);
      return '';
    }
  }

  isAIPackage(packageName) {
    const aiKeywords = [
      'ai',
      'ml',
      'tensorflow',
      'torch',
      'sklearn',
      'pandas',
      'numpy',
      'opencv',
      'nltk',
      'spacy',
      'transformers',
      'langchain',
      'openai',
      'anthropic',
      'huggingface',
      'azure-cognitiveservices',
      'aws-sdk',
      'google-cloud-ai',
    ];

    return aiKeywords.some(keyword =>
      packageName.toLowerCase().includes(keyword)
    );
  }

  parseRequirementsTxt(content) {
    const deps = {};
    const lines = content.split('\n');

    for (const line of lines) {
      const match = line.match(/^([a-zA-Z0-9\-_]+)([>=<]+.*)?$/);
      if (match) {
        deps[match[1]] = match[2] || 'latest';
      }
    }

    return deps;
  }

  parseToml(content) {
    // Simple TOML parser for basic dependencies
    const deps = {};
    const lines = content.split('\n');

    for (const line of lines) {
      const match = line.match(
        /^\s*([a-zA-Z0-9\-_]+)\s*=\s*["']?([^"']+)["']?/
      );
      if (match) {
        deps[match[1]] = match[2];
      }
    }

    return deps;
  }

  /**
   * Report generation
   */
  generateReport() {
    const report = {
      timestamp: new Date().toISOString(),
      summary: this.generateSummary(),
      details: {
        languages: Array.from(this.findings.languages),
        frameworks: Array.from(this.findings.frameworks),
        aiLibraries: Array.from(this.findings.aiLibraries),
        aiServices: Array.from(this.findings.aiServices),
        aiConfigurations: Array.from(this.findings.aiConfigurations),
        aiGeneratedPatterns: Array.from(this.findings.aiGeneratedPatterns),
        infrastructure: Array.from(this.findings.infrastructure),
        dependencies: Object.fromEntries(this.findings.dependencies),
      },
      scores: this.calculateScores(),
      recommendations: this.generateRecommendations(),
    };

    return report;
  }

  generateSummary() {
    return {
      totalLanguages: this.findings.languages.size,
      totalFrameworks: this.findings.frameworks.size,
      totalAILibraries: this.findings.aiLibraries.size,
      totalAIServices: this.findings.aiServices.size,
      totalAIConfigs: this.findings.aiConfigurations.size,
      totalAIGeneratedFiles: this.findings.aiGeneratedPatterns.size,
      totalInfraComponents: this.findings.infrastructure.size,
      totalDependencies: this.findings.dependencies.size,
    };
  }

  calculateScores() {
    const scores = {
      technologyStack: this.calculateTechStackScore(),
      aiLibraryIntegration: this.calculateAILibraryScore(),
      aiServiceIntegration: this.calculateAIServiceScore(),
      infrastructureReadiness: this.calculateInfraScore(),
      overall: 0,
    };

    // Calculate overall score
    scores.overall =
      scores.technologyStack * 0.3 +
      scores.aiLibraryIntegration * 0.3 +
      scores.aiServiceIntegration * 0.25 +
      scores.infrastructureReadiness * 0.15;

    return scores;
  }

  calculateTechStackScore() {
    const aiFriendlyLanguages = [
      'Python',
      'JavaScript',
      'TypeScript',
      'Java',
      'C#',
    ];
    const aiFriendlyFrameworks = [
      'TensorFlow',
      'PyTorch',
      'scikit-learn',
      'FastAPI',
    ];

    let score = 0;

    // Language compatibility (40%)
    const compatibleLangs = Array.from(this.findings.languages).filter(lang =>
      aiFriendlyLanguages.includes(lang)
    );
    score +=
      (compatibleLangs.length / Math.max(this.findings.languages.size, 1)) * 40;

    // Framework support (30%)
    const compatibleFrameworks = Array.from(this.findings.frameworks).filter(
      fw => aiFriendlyFrameworks.includes(fw)
    );
    score +=
      (compatibleFrameworks.length /
        Math.max(this.findings.frameworks.size, 1)) *
      30;

    // AI library presence (30%)
    score += Math.min(this.findings.aiLibraries.size * 10, 30);

    return Math.round(score);
  }

  calculateAILibraryScore() {
    const coreLibraries = [
      'TensorFlow',
      'PyTorch',
      'scikit-learn',
      'OpenAI',
      'LangChain',
    ];
    const presentCoreLibs = coreLibraries.filter(lib =>
      this.findings.aiLibraries.has(lib)
    );

    let score = 0;

    // Core library presence (50%)
    score += (presentCoreLibs.length / coreLibraries.length) * 50;

    // Additional libraries (30%)
    score += Math.min(this.findings.aiLibraries.size * 5, 30);

    // AI-generated code patterns (20%)
    score += Math.min(this.findings.aiGeneratedPatterns.size * 2, 20);

    return Math.round(score);
  }

  calculateAIServiceScore() {
    const majorServices = ['OpenAI', 'Azure AI', 'Google AI', 'AWS AI'];
    const presentServices = majorServices.filter(service =>
      this.findings.aiServices.has(service)
    );

    let score = 0;

    // Major service integration (60%)
    score += (presentServices.length / majorServices.length) * 60;

    // Configuration files (40%)
    score += Math.min(this.findings.aiConfigurations.size * 10, 40);

    return Math.round(score);
  }

  calculateInfraScore() {
    const infraComponents = [
      'Docker',
      'Kubernetes',
      'GPU',
      'Cloud AI',
      'CI/CD',
    ];
    const presentInfra = infraComponents.filter(comp =>
      this.findings.infrastructure.has(comp)
    );

    let score = 0;

    // Infrastructure components (70%)
    score += (presentInfra.length / infraComponents.length) * 70;

    // Automation (30%)
    const hasAutomation = this.findings.infrastructure.has('CI/CD');
    score += hasAutomation ? 30 : 0;

    return Math.round(score);
  }

  generateRecommendations() {
    const recommendations = [];
    const scores = this.calculateScores();

    if (scores.technologyStack < 70) {
      recommendations.push({
        priority: 'high',
        category: 'technology',
        title: 'Upgrade Technology Stack for AI Readiness',
        description: 'Consider adopting AI-friendly languages and frameworks',
        actions: [
          'Add Python for ML/AI development',
          'Integrate modern JavaScript/TypeScript frameworks',
          'Evaluate AI-compatible database solutions',
        ],
      });
    }

    if (scores.aiLibraryIntegration < 60) {
      recommendations.push({
        priority: 'medium',
        category: 'libraries',
        title: 'Enhance AI Library Integration',
        description: 'Add core AI/ML libraries to your technology stack',
        actions: [
          'Integrate TensorFlow or PyTorch for ML capabilities',
          'Add OpenAI or Anthropic SDKs for AI services',
          'Include data processing libraries (pandas, numpy)',
        ],
      });
    }

    if (scores.aiServiceIntegration < 50) {
      recommendations.push({
        priority: 'medium',
        category: 'services',
        title: 'Implement AI Service Integration',
        description: 'Configure and integrate cloud AI services',
        actions: [
          'Set up OpenAI or Azure AI service accounts',
          'Create AI service configuration files',
          'Implement API key management',
        ],
      });
    }

    if (scores.infrastructureReadiness < 60) {
      recommendations.push({
        priority: 'low',
        category: 'infrastructure',
        title: 'Optimize Infrastructure for AI Workloads',
        description: 'Enhance infrastructure to support AI development',
        actions: [
          'Add Docker containerization for AI models',
          'Consider GPU-enabled environments',
          'Implement CI/CD for AI pipelines',
        ],
      });
    }

    return recommendations;
  }
}

// CLI execution
if (require.main === module) {
  const repoPath = process.argv[2] || '.';
  const detector = new AITechnologyDetector(repoPath);

  detector
    .detect()
    .then(report => {
      console.log('\n📊 AI Technology Detection Report');
      console.log('=====================================');
      console.log(JSON.stringify(report, null, 2));
    })
    .catch(error => {
      console.error('❌ Detection failed:', error);
      process.exit(1);
    });
}

module.exports = AITechnologyDetector;
